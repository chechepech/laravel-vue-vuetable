	public function get(){
		$objectives = Objective::all();
		$otherObjectives = OtherObjective::all();

		foreach ($objectives as $key => $value) {
			$value->index = $value->id .',Objective';
		}

		foreach ($otherObjectives as $key => $value) {
			$value->index = $value->id .',otherObjective';
		}

		$result = array_merge($objectives->toArray(),$otherObjectives->toArray());

		return $result;
	}
